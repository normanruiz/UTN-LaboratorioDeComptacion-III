CREATE TABLE VECINOS(
	DNI VARCHAR(10) NOT NULL PRIMARY KEY,
	APELLIDOS VARCHAR(50) NOT NULL,
	NOMBRES VARCHAR(50) NOT NULL,
	GENERO CHAR NOT NULL
)
GO
CREATE TABLE TIPOS_PROPIEDADES(
	ID SMALLINT NOT NULL PRIMARY KEY,
	TIPO VARCHAR(50) NOT NULL
)
GO
CREATE TABLE PROPIEDADES(
	ID INT NOT NULL PRIMARY KEY,
	IDTIPO SMALLINT NOT NULL FOREIGN KEY REFERENCES TIPOS_PROPIEDADES(ID),
	DNI VARCHAR(10) NOT NULL FOREIGN KEY REFERENCES VECINOS(DNI),
	DOMICILIO VARCHAR(150) NULL,
	SUPERFICIE DECIMAL(10, 2) NOT NULL,
	SUPERFICIE_CONSTRUIDA DECIMAL(10, 2) NOT NULL,
	VALOR MONEY NOT NULL,
	ANTIGUEDAD SMALLINT NOT NULL
)
GO
